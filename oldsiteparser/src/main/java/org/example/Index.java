package org.example;

public class Index {

    private String recomendada;

    private String tituloBienvenida = "NOTSET";

    private String textoBienvenida;

    private String ultimasExcursiones;

    public String getRecomendada() {
        return recomendada;
    }

    public void setRecomendada(String recomendada) {
        this.recomendada = recomendada;
    }

    public String getTituloBienvenida() {
        return tituloBienvenida;
    }

    public void setTituloBienvenida(String tituloBienvenida) {
        this.tituloBienvenida = tituloBienvenida;
    }

    public String getTextoBienvenida() {
        return textoBienvenida;
    }

    public void setTextoBienvenida(String textoBienvenida) {
        this.textoBienvenida = textoBienvenida;
    }

    public String getUltimasExcursiones() {
        return ultimasExcursiones;
    }

    public void setUltimasExcursiones(String ultimasExcursiones) {
        this.ultimasExcursiones = ultimasExcursiones;
    }
}
